package javacodes;

public class IfElseStatementExample {

	public static void main(String[] args) {
		int x = 6;
		
		if(x==5){
			System.out.println("The value of x is 5");
		}
		else if(x == 6) {
			System.out.println("The value of x is 6");
		}
		else {
			System.out.println("The value of x is not 5");
		}

	}

}
